export default function Speakers() {
  const [index, setIndex] = useState(0);
  const [open, setOpen] = useState(false)
  const [person, setPerson] = useState(people[index]);
  const openModal = (person: any, i: number) => {
    setIndex(i);
    setPerson(person);
    setOpen(true);
  }

  const previous = () => {
    if (index > 0) {
      setPerson(people[index - 1]);
      setIndex(index - 1);
    }
  }
  const next = () => {
    if (index < people.length - 1) {
      setPerson(people[index + 1]);
      setIndex(index + 1);
    }
  }

  return (
    <div className="mx-auto max-w-4xl py-12 px-4 sm:px-6 lg:py-24 lg:px-8">
      <div className="pb-12 text-center">
        <h2 className="text-3xl font-bold tracking-tight text-gray-100 md:text-4xl">
          Speakers 2022
        </h2>
      </div>

      <ul role="list" className="grid grid-cols-1 gap-6 sm:grid-cols-2 md:grid-cols-3">
        {people.map((person, i) => (
          <li
            key={person.name}
            className="col-span-1 flex flex-col rounded-lg text-center shadow text-white bg-gray-900 gradient-background-hover hover:shadow-xl hover:shadow-blue-900/50 transition transition-all"
          >
            <a href="#" onClick={(e: any) => { e.preventDefault(); openModal(person, i) }}>
              <div className="flex flex-1 flex-col p-8">
                <img className="mx-auto h-32 w-32 flex-shrink-0 rounded-full" src={person.imageUrl} alt="" />
                <h3 className="mt-6 text-sm font-bold text-white">{person.name}</h3>
                <dl className="mt-1 flex flex-grow flex-col justify-between">
                  <dt className="sr-only">Title</dt>
                  <dd className="text-sm text-gray-100">{person.title}</dd>
                </dl>
              </div>
            </a>
          </li>
        ))}
      </ul>

      <Transition.Root show={open} as={Fragment}>
        <Dialog as="div" className="relative z-10" onClose={setOpen}>
          <Transition.Child
            as={Fragment}
            enter="ease-out duration-300"
            enterFrom="opacity-0"
            enterTo="opacity-100"
            leave="ease-in duration-200"
            leaveFrom="opacity-100"
            leaveTo="opacity-0"
          >
            <div className="fixed inset-0 bg-gray-500 bg-opacity-75 transition-opacity" />
          </Transition.Child>

          <div className="fixed inset-0 z-10 overflow-y-auto">
            <div className="flex min-h-full items-end justify-center p-4 text-center sm:items-center sm:p-0">
              <Transition.Child
                as={Fragment}
                enter="ease-out duration-300"
                enterFrom="opacity-0 translate-y-4 sm:translate-y-0 sm:scale-95"
                enterTo="opacity-100 translate-y-0 sm:scale-100"
                leave="ease-in duration-200"
                leaveFrom="opacity-100 translate-y-0 sm:scale-100"
                leaveTo="opacity-0 translate-y-4 sm:translate-y-0 sm:scale-95"
              >
                <Dialog.Panel className="relative transform overflow-hidden rounded-lg gradient-background px-4 pt-5 pb-4 text-left shadow-xl transition-all sm:my-8 sm:w-full sm:max-w-sm sm:p-6">
                  <div>
                    <div className="mt-3 text-center sm:mt-5">
                      <div className="flex flex-1 flex-col px-8">
                        <img className="mx-auto h-32 w-32 flex-shrink-0 rounded-full" src={person.imageUrl} alt="" />

                        <Dialog.Title as="h3" className="mt-6 text-lg font-bold leading-6 text-white">
                          {person.name}
                        </Dialog.Title>


                        <dl className="mt-4 flex flex-grow flex-col justify-between">
                          <dt className="sr-only">Title</dt>
                          <dd className="mb-4 font-bold text-md text-gray-200">{person.title}</dd>
                          <dt className="sr-only">Description</dt>
                          <dd className="text-sm text-left text-gray-200">{person.description}</dd>
                        </dl>

                        <div className="mt-6 flex justify-center space-x-6 md:order-2">
                          {person.socials.map((item) => (
                            <a key={item.name} href={item.href} className="text-gray-300 hover:text-white">
                              <span className="sr-only">{item.name}</span>
                              <item.icon className="h-6 w-6" aria-hidden="true" />
                            </a>
                          ))}
                        </div>
                      </div>
                      <div className="mt-6 sm:mt-6 flex justify-center">
                        <a href="#" className="p-2 hover:underline hover:underline-offset-4 text-gray-100 rounded" onClick={(e) => { e.preventDefault(); previous() }}>
                          previous
                        </a>
                        <a href="#" className="p-2 hover:underline hover:underline-offset-4 text-gray-100 rounded" onClick={(e) => { e.preventDefault(); next() }}>
                          next
                        </a>
                      </div>
                    </div>
                  </div>
                </Dialog.Panel>
              </Transition.Child>
            </div>
          </div>
        </Dialog>
      </Transition.Root>
    </div>
  )
}
